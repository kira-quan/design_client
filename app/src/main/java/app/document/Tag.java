package app.document;


public class Tag {
	
	String name;

	//----------------------- Constructors -------------------------------------
	
	public Tag(String name) {
		super();
		this.name = name;
	}

	//----------------------- Getters and Setters ------------------------------
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
